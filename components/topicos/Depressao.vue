    <h2>Que é o Depressão?</h2>

<p>

A depressão é um transtorno comum, mas sério, que interfere na vida diária, capacidade de trabalhar, dormir, estudar, comer e aproveitar a vida. É causada por uma combinação
de fatores genéticos, biológicos, ambientais e psicológicos.
Algumas pesquisas genéticas indicam que o risco de depressão resulta da influência de vários genes que atuam em conjunto com fatores ambientais ou outros.
Alguns tipos de depressão tendem a ocorrer em famílias. No entanto, a depressão também pode ocorrer em pessoas sem histórico familiar do transtorno. Nem todas as pessoas
com transtornos depressivos apresentam os mesmos sintomas. A gravidade, frequência e duração variam dependendo do indivíduo e de sua condição específica. </p>
