    <h3> O que é o Transtrono de Estresse Pós-Traumático?</h3>
    <br/>
<p>

O transtorno de estresse pós-traumático (TSPT) é um tipo de transtorno de ansiedade que pode se desenvolver em pessoas que vivenciaram um evento traumático.
Essa condição causa sofrimento intenso e prejuízos a vários aspectos da vida, como trabalho e relacionamentos. Saiba quais as opções comprovadamente eficazes de tratamento,
como a psicoterapia.
Quem pode ter transtorno de estresse pós-traumático
Vários tipos de experiências aterrorizantes ou ameaçadoras podem levar ao surgimento do estresse pós-traumático, tais como:
<br/>
<br/>

<ul>

<li>Violência física ou sexual;</li>
<li>Assalto ou sequestro;</li>
<li>Acidente de carro;</li>
<li>Desastres naturais;</li>
<li>Diagnóstico de doença que ameace a vida.</li>

</ul>

<br/>
<br/>


Vale destacar que mesmo quem não foi vítima direta de tais situações pode receber o diagnóstico. Testemunhar uma agressão ou ser informado que um familiar sofreu um acidente grave, 
por exemplo, também são possíveis fatores desencadeantes.
Embora qualquer pessoa exposta a algum tipo de trauma psicológico possa desenvolver o distúrbio, mulheres são duas vezes mais atingidas do que homens.
<br/>
<br/>


<strong>Sintomas do Transtorno de Estresse Pós-Traumático:</strong>
</br></br>

É natural ficar abalado na sequência de uma experiência como as citadas acima. Para algumas pessoas, no entanto, os sentimentos e pensamentos perturbadores associados ao episódio
persistem por meses ou mesmo anos, o que pode indicar um quadro de estresse pós-traumático. A pessoa pode apresentar sintomas como:
<br/>
<br/>

<ul>
    <li>Lembranças persistentes reviver involuntariamente o trauma através de memórias angustiantes e repetitivas, pesadelos ou sensação de que o evento traumático está acontecendo novamente (“flashbacks”).</li>
    <li>Reações físicas circunstâncias que remetem à lembrança do trauma podem desencadear sintomas fisiológicos, incluindo sudorese, náusea e tremores.</li>
    <li>Comportamento de esquiva evitar lugares, pessoas e atividades que trazem recordações dolorosas. A pessoa também pode ser incapaz de lembrar ou falar sobre o ocorrido.</li>
    <li>Excitação exagerada inclui ficar em estado de alerta constante, explosões de raiva, dificuldade para dormir e para se concentrar.
        Crenças e emoções negativas - por exemplo, dificuldade de confiar nos outros e manter relacionamentos próximos; perda de interesse em atividades; sentimento de culpa e vergonha.</li>
</ul>

<br/>


