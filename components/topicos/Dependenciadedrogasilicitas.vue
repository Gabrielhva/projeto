        <li>Dificuldades em controlar o comportamento, tanto no que se refere ao início, fim e níveis de consumo;</li>
        <li>Estado de abstinência fisiológico quando o uso cessa ou é reduzido;</li>
        <li>Evidência de tolerância, na qual doses crescentes são necessárias para se obter um efeito inicialmente obtido com dosagens mais baixas;</li>
        <li>Abandono progressivo de prazeres e interesses alternativos em favor do seu uso e aumento da quantidade de tempo necessária para se recuperar dos seus efeitos;</li>
