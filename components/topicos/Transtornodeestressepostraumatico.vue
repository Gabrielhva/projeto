</br></br>

O tratamento do transtorno de estresse pós-traumático pode ajudar adultos e crianças a ganhar o controle do medo e da angústia e a lidar com outros sintomas. Não hesite em consultar
um profissional de saúde mental capacitado para que, juntos, vocês possam decidir a abordagem ou abordagens mais adequadas para suas necessidades. O tratamento do transtorno de estresse
pós-traumático pode envolver:
<br/>
<br/>

<strong>Psicoterapia:</strong> existem alguns tipos de psicoterapia úteis para o tratamento do transtorno pós-traumático. Algumas delas são focadas no trauma, ou seja, é dada ao paciente a 
oportunidade de compartilhar sua experiência. Embora o ato de recordar um evento traumático possa parecer intrusivo e desagradável, a exposição repetida a essas memórias de maneira segura e
controlada é um mecanismo para enfrentar e superar o trauma
<br/>
<br/>

<strong>Psicoterapia  Medicação: </strong>antidepressivos, ansiolíticos e outros tipos de medicamentos são comumente usados para aliviar os sintomas do TSPT. Podem ser usados sozinhos ou
junto com psicoterapia ou outros tratamentos
