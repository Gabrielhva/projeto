    <strong>Causas:</strong>
    <p>
   
        Relaciona-se com as características químicas da droga ou da substância, com o perfil psicológico do consumidor e com o meio ambiente onde ele está inserido.
        No que se refere às características, o tabaco e o café, por exemplo, não alteram de forma significativa o comportamento do consumidor e, por isso, são compatíveis com uma vida social normal. No entanto, o tabaco pode não dar alterações de comportamento mas pode provocar problemas graves da saúde.<br/>
        No que se refere às características psicológicas do consumidor, a adolescência é uma fase difícil para cerca de um terço dos jovens que pode provocar frustrações que impelem à fuga para estas substâncias. A adolescência é, por definição, uma época de experimentação e de procura dos limites do "eu", o que leva frequentemente à iniciação nas drogas.<br/><br/>
        O género masculino tende a ser o mais afetado pelo seu consumo, embora as mulheres, depois da iniciação, tenha um percurso mais rápido e degradante.

A baixa tolerância à frustração, uma reduzida autoestima, a necessidade de aprovação social, a não aceitação sistemática de normas sociais, a predisposição para doenças mentais, são outras características que tornam algumas pessoas mais suscetíveis de recorrerem a estas substâncias e de se tornarem dependentes delas.

Importa referir que os canabinóides são relativamente inofensivos mas, em determinados indivíduos predispostos, podem acelerar a instalação de doenças mentais graves, como as psicoses. A cocaína, as anfetaminas, o ecstasy e o LSD podem também induzir crises psicóticas em determinados consumidores. Por outro lado, parece existir uma predisposição genética para a dependência.

No que se refere ao meio ambiente, o papel da família, da escola, do local de trabalho pode ser determinante no aparecimento de uma dependência ou, inversamente, na sua deteção e correção precoces.  
    </p>

    <br/>
    <strong>Diagnóstico:</strong>
    <p>O diagnóstico começa frequentemente a nível do médico de família ou dos familiares mas deve ser confirmado por um psiquiatra, de acordo com os critérios internacionalmente definidos. A pesquisa de substâncias no sangue não permite fazer o diagnóstico mas tem como objetivo verificar se ocorreu consumo recente.</p>

    <br/>
    <strong>Tratamento:</strong>
    <p>O tratamento deverá ser sempre individualizado. Nalguns casos, um regime de internamento é eficaz porque permite interromper a progressão e o agravamento dos sintomas associados à doença, bem como todo o seu impacto na saúde do paciente, na sua família, no trabalho ou nas suas finanças.

Por outro lado, deve englobar uma componente psicossocial, onde o indivíduo reaprende novas competências e aptidões que lhe proporcionem mais qualidade de vida.

A terapêutica psicossocial inclui todo o processo de avaliação e diagnóstico, num ambiente de tratamento seguro e livre de drogas lícitas e ilícitas, com apoio farmacológico e supervisão permanentes, um plano personalizado e estruturado de intervenção, psicoterapia individual e de grupo, educação sobre as dependências e sobre a abstinência, atividades sociais, programas familiares e uma fase de pós-tratamento/integração, já em regime de ambulatório durante um ano.

Todas estas etapas visam a recuperação plena do indivíduo dependente e a sua reintegração na comunidade. Este trabalho pode e deve ser complementado pelo tratamento médico de todas as complicações e/ou doenças que possam estar associadas ou ser causadas pela dependência de substâncias. Os fármacos são igualmente essenciais para compensar os efeitos da abstinência e permitir uma recuperação menos penosa.</p>
<br/>
     <strong>Prevenção:</strong>   
     <p>A melhor maneira de prevenir a dependência de uma substância é não a consumir. Uma vez que pode surgir mesmo para medicamentos prescritos pelo médico, é importante respeitar as doses e informar o profissional de saúde de quaisquer sintomas de dependência.

Nos adolescentes, a prevenção passa por uma boa capacidade de comunicação dentro da família, pela atenção constante a sinais de alerta, por saber ouvir e por saber dar o exemplo.

Nos casos de indivíduos recuperados, existe sempre o risco de recaída pelo que se deve evitar o contacto com a substância bem como todas as situações de risco.</p>
