<h2>Principais fatos</h2>
</br>

A depressão é um transtorno mental frequente. Em todo o mundo, estima-se que mais de 300 milhões de pessoas, de todas as idades, sofram com esse transtorno.
A depressão é a principal causa de incapacidade em todo o mundo e contribui de forma importante para a carga global de doenças.
Mulheres são mais afetadas que homens.
No pior dos casos, a depressão pode levar ao suicídio.
Existem vários tratamentos medicamentosos e psicológicos eficazes para depressão. 
</br></br>

<h3>Tipos de sintomas</h3>
</br>

Um episódio depressivo pode ser categorizado como leve, moderado ou grave, a depender da intensidade dos sintomas. Um indivíduo com um episódio depressivo leve
terá alguma dificuldade em continuar um trabalho simples e atividades sociais, mas sem grande prejuízo ao funcionamento global. Durante um episódio depressivo grave,
é improvável que a pessoa afetada possa continuar com atividades sociais, de trabalho ou domésticas.
</br>

Uma distinção fundamental também é feita entre depressão em pessoas que têm ou não um histórico de episódios de mania. Ambos os tipos de depressão podem ser crônicos
(isto é, acontecem durante um período prolongado), com recaídas, especialmente se não forem tratados.
</br></br>

<strong>Transtorno depressivo recorrente</strong> esse distúrbio envolve repetidos episódios depressivos. Durante esses episódios, a pessoa experimenta um humor deprimido, perda de interesse
e prazer e energia reduzida, levando a uma diminuição das atividades em geral por pelo menos duas semanas. Muitas pessoas com depressão também sofrem com sintomas como ansiedade,
distúrbios do sono e de apetite e podem ter sentimento de culpa ou baixa autoestima, falta de concentração e até mesmo aqueles que são clinicamente inexplicáveis.
</br></br>

<strong>Transtorno afetivo bipolar</strong> esse tipo de depressão consiste tipicamente na alternância entre episódios de mania e de depressão, separados por períodos de humor normal. Episódios de mania envolvem
humor exaltado ou irritado, excesso de atividades, pressão de fala, autoestima inflada e uma menor necessidade de sono, bem como a aceleração do pensamento.
</br></br>

<h4>Fatores que contribuem e prevenção</h4>
</br>

A depressão é resultado de uma complexa interação de fatores sociais, psicológicos e biológicos. Pessoas que passaram por eventos adversos durante a vida (desemprego, luto, trauma psicológico) são mais propensas a 
desenvolver depressão. A depressão pode, por sua vez, levar a mais estresse e disfunção e piorar a situação de vida da pessoa afetada e o transtorno em si.
Há relação entre a depressão e a saúde física; doenças cardiovasculares, por exemplo, podem levar à depressão e vice e versa.
</br></br>

<h5>Diagnóstico e tratamento</h5>        
</br>

Existem tratamentos eficazes para depressão moderada e grave. Profissionais de saúde podem oferecer tratamentos psicológicos, como ativação comportamental, terapia cognitivo-comportamental e psicoterapia interpessoal ou
medicamentos antidepressivos. Os provedores de saúde devem ter em mente a possibilidade de efeitos adversos associados aos antidepressivos, a possibilidade de oferecer um outro tipo de intervenção (por disponibilidade de
conhecimentos técnicos ou do tratamento em questão) e preferências individuais. Entre os diferentes tratamentos psicológicos a serem considerados estão os individuais ou em grupo, realizados por profissionais ou terapeutas
leigos supervisionados.
Os tratamentos psicossociais também são efetivos para depressão leve. Os antidepressivos podem ser eficazes no caso de depressão moderada-grave, mas não são a primeira linha de tratamento para os casos mais brandos. Esses medicamentos
não devem ser usados para tratar depressão em crianças e não são, também, a primeira linha de tratamento para adolescentes. É preciso utilizá-los com cautela.
</br></br>

