    <strong>Sintomas:</strong>
    <br/>
    <p>Muitas iniciam-se de uma forma meramente casual, num determinado contexto social. Esse consumo vai-se tornando mais habitual até adquirir as características de uma dependência, requerendo quantidades cada vez maiores e com uma utilização cada vez mais frequente. <br/>
        Ao longo do tempo, essa substância torna-se essencial para que a pessoa se sinta bem e capaz de ser funcional no seu dia-a-dia. Sendo que a ausência gera desconforto físico e emocional (abstinência).<br/>
        As manifestações típicas passam pela sensação de necessidade de um consumo cada vez mais regular, pela incapacidade de o interromper, pela indispensabilidade de manter sempre uma reserva da substância, pelo gasto excessivo de dinheiro, muitas vezes acima das possibilidades, pela impressão de que o seu uso é essencial para se conseguir ultrapassar os problemas diários, pela realização de atividades arriscadas sob influência desse elemento e pelo crescente gasto de tempo e energia na sua obtenção e utilização.<br/>
        Por vezes, é difícil distinguir a dependência de substâncias das alterações de comportamento frequentes na adolescência. Num adolescente, deve-se suspeitar deste problema se ocorrerem problemas na escola (faltas, desinteresse, mau desempenho), ausência de energia ou de motivação, aspeto pouco cuidado, alterações no comportamento ou gasto excessivo de dinheiro. 
    </p>

